    private static int insertion(admin admin){
         try {
            int a = 0;
            String insertion = "insert into admin(nom_admin,prenom,login,password,email) values(?,?,?,?,?)";

            db.iniPreparedCmd(insertion);
            db.getPstmt().setString(4, admin.getPassword());
            db.getPstmt().setString(2, admin.getPrenom());
            db.getPstmt().setString(1, admin.getNom());
            db.getPstmt().setString(3, admin.getLogin());
            db.getPstmt().setString(5, admin.getEmail());

            a = db.executePreparedCUD();

            if (a != 0) {
                return 1;
            }

        } catch (Exception ex) {
            Logger.getLogger(inscriptionAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }
    
    private static int verificationLogin(String login,String email){
    try{
        String recuplogin=String.format("select id from admin where login='%s'", login);
        String recupemail=String.format("select id from admin where email='%s'", email);
        db.iniPreparedCmd(recuplogin);
        ResultSet log= db.executeSelect(recuplogin);
        if(log.next()){
            return 1;
        }
        db.iniPreparedCmd(recupemail);
        ResultSet mail= db.executeSelect(recupemail);
        if(mail.next()){
            return 2;
        }

        } catch (Exception ex) {
           Logger.getLogger(inscriptionAdmin.class.getName()).log(Level.SEVERE, null, ex);
       }
    return -1;
    }
    
